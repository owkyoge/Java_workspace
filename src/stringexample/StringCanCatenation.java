package stringexample;

public class StringCanCatenation {

	public static void main(String[] args) {

		int i = 10;
		int j = 20;
		int k = i + j;
		System.out.println(k);

		String s1 = "Hello";
		String s2 = "World";
		String s3 = s1 + s2;

		System.out.println(s3);

		String s4 = s1 + i;

		System.out.println(s4);

		String s5 = i + j + s2;

		System.out.println(s5);

		String s6 = s1 + i + j;

		System.out.println(s6);

		
		
		
		
		
		
		
		
	}

}
